	public static void maim(String[] args) {
		
	//논리 부정 연산자 -> !
		
		boolean b1 = true;
		boolean b2 = !b1;
		
		System.out.println("논리 부정 연산 결과 : " + b2);
		
		// 증감 연산자 -> ++,--
	  int i1 = 1;
	  int i2 = ++i1;
	  int i3 = --i1;
	   System.out.println("증강 연산 결과 : " + i2);
	   System.out.println("증강 연산 결과 : " + i3);
	   
	   
	}
